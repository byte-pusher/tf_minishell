#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*p;

	if (lst && f)
	{
		while (lst)
		{
			(*f)(lst->content);
			p = lst->next;
			lst = p;
		}
	}
}
